// Writes src/deploy-stamp.js with build-time values the Worker can't read at
// runtime. Run by the npm pre-deploy / pre-dev hooks:
//   node scripts/generate-build-timestamp.mjs deploy   → deploy timestamp
//   node scripts/generate-build-timestamp.mjs dev       → latest src edit + last commit
// The Worker picks which set to show at request time based on hostname.
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { execSync } from "node:child_process";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outFile = join(root, "src", "deploy-stamp.js");
const mode = process.argv[2] === "deploy" ? "deploy" : "dev";
const empty = { date: "", time: "" };

// A Date → { date: "YYYY-MM-DD", time: "HH:MM:SS" } in local time.
function parts(d) {
  const p = (n) => String(n).padStart(2, "0");
  return {
    date: `${d.getFullYear()}-${p(d.getMonth() + 1)}-${p(d.getDate())}`,
    time: `${p(d.getHours())}:${p(d.getMinutes())}:${p(d.getSeconds())}`,
  };
}

// Newest mtime under src/, excluding the generated file itself.
function newestSrcMtime(dir, acc) {
  for (const ent of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, ent.name);
    if (ent.isDirectory()) newestSrcMtime(full, acc);
    else if (full !== outFile) acc.push(statSync(full).mtimeMs);
  }
  return acc;
}

const stamp = { mode, deploy: empty, pageEdit: empty, commit: empty };

if (mode === "deploy") {
  stamp.deploy = parts(new Date());
} else {
  const mtimes = newestSrcMtime(join(root, "src"), []);
  if (mtimes.length) stamp.pageEdit = parts(new Date(Math.max(...mtimes)));
  try {
    const ct = execSync("git log -1 --format=%ct", { cwd: root }).toString().trim();
    if (ct) stamp.commit = parts(new Date(Number(ct) * 1000));
  } catch {
    // no commits yet / not a repo → leave commit empty
  }
}

const body =
  "// Generated by scripts/generate-build-timestamp.mjs — do not edit by hand.\n" +
  `export const buildStamp = ${JSON.stringify(stamp, null, 2)};\n`;
writeFileSync(outFile, body);
console.log(`wrote src/deploy-stamp.js (${mode})`);
